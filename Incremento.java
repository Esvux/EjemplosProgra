public class Incremento {

	public static void main(String[] args) {
		int a = 10;
		System.out.print("'a' vale inicialmente: ");
		System.out.println(a);
		//10

		System.out.print("Con el pre incremento 'a' vale: ");
		System.out.println(++a);
		//11

		System.out.print("Al final 'a' vale: ");
		System.out.println(a);
		//11
	}
}
