public class Raices {

	public static void main(String []args) {
		System.out.println("¿Cuál es la raíz cuarta de 256?");
		
		System.out.println("Francisco dice: "); //Aniver
		System.out.println(Math.sqrt(Math.sqrt(256)));
		
		System.out.println("Domingo dice: "); //Pablo
		System.out.println(Math.pow(256, -4));
		
		System.out.println("Esvin dice: "); //Jorge, Pedro
		System.out.println(Math.pow(256, 0.25));

	}

}